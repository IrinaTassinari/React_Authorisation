// Импортируем компонент Navigate из react-router-dom.
// Navigate - это специальный компонент для "редиректа" (перенаправления).
//
// Когда React рендерит <Navigate to="/login" />,
// роутер автоматически меняет текущий URL на "/login".
//
// Важно: это "React-способ" делать редирект через JSX,
// а не через window.location (который перезагружает страницу).
import { Navigate } from 'react-router-dom'

// Импортируем useSelector из react-redux.
// useSelector позволяет читать данные из Redux store.
// Мы будем смотреть, есть ли token в state.auth (то есть авторизован ли пользователь).
import { useSelector } from 'react-redux'

/*
  ProtectedRoute - компонент "защищённого маршрута".

  Идея простая:
  - Если пользователь авторизован (есть token) -> показать страницу (children)
  - Если не авторизован -> перенаправить на страницу /login

  children - это то, что мы "оборачиваем" в ProtectedRoute.
  Пример использования:

  <Route
    path="/profile"
    element={
      <ProtectedRoute>
        <Profile />
      </ProtectedRoute>
    }
  />

  В этом примере:
  children = <Profile />
*/
const ProtectedRoute = ({ children }) => {
  // Достаём token из Redux store.
  //
  // state.auth - это auth slice (редьюсер auth), который мы подключили в store:
  // reducer: { auth: authReducer }
  //
  // Значит структура store такая:
  // state = { auth: { token: ..., user: ..., isLoading: ... } }
  //
  // token:
  // - если есть -> пользователь "вошёл" (авторизован)
  // - если null -> пользователь "не вошёл" (не авторизован)
  const { token } = useSelector((state) => state.auth)

  // Проверяем авторизацию.
  // Если токена нет - доступ к странице запрещён.
  if (!token) {
    // Возвращаем редирект на /login.
    //
    // replace означает:
    // "замени текущую запись в истории браузера"
    //
    // Почему это полезно?
    // Представь:
    // - пользователь ввёл вручную /profile (не будучи авторизован)
    // - его перекинуло на /login
    //
    // Если replace НЕ использовать, то в истории будет:
    // /profile -> /login
    // и кнопка "Назад" снова вернёт на /profile,
    // снова редиректнёт на /login, и получится странный цикл.
    //
    // replace делает так, что "плохая" страница (/profile) не остаётся в истории.
    return <Navigate to="/login" replace />
  }

  // Если token существует - значит пользователь авторизован.
  // Тогда разрешаем доступ и просто рендерим "детей" (страницу, которую защищаем).
  //
  // Например, children = <Profile />
  return children
}

// Экспортируем ProtectedRoute по умолчанию,
// чтобы его можно было импортировать в роуты:
export default ProtectedRoute

/*
  Важно:

  1) ProtectedRoute защищает ТОЛЬКО те страницы, которые ты обернул в него.
     Если хочешь защитить страницу /settings - тоже оборачивай её.

  2) Проверка здесь очень простая: просто наличие token.
     В реальных проектах могут проверять ещё:
     - срок действия токена (exp)
     - роль пользователя (admin/user)
     - статус (ban/active)
     Но для учебного проекта - этого достаточно.

  3) ProtectedRoute не делает запросы на сервер.
     Он просто решает: "Показывать страницу или отправить на /login".
*/
