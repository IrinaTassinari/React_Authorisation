// Импортируем функцию jwtDecode из библиотеки "jwt-decode".
// JWT (JSON Web Token) - это строка формата:
// header.payload.signature
//
// Внутри "payload" обычно лежит информация, например:
// - user id
// - email
// - время создания токена (iat)
// - время истечения токена (exp)
//
// jwtDecode(token) НЕ проверяет подпись токена и НЕ проверяет,
// настоящий ли токен (это делает сервер).
// jwtDecode просто "читает" payload и возвращает обычный JS-объект.
import { jwtDecode } from 'jwt-decode'

// Функция isTokenExpired проверяет: истёк токен или ещё действует.
//
// Она принимает token (строку JWT) и возвращает:
// - true  -> токен истёк (пользователь должен разлогиниться)
// - false -> токен ещё действителен
export const isTokenExpired = (token) => {
    // Если токена вообще нет (null/undefined/пустая строка),
    // значит пользователь не авторизован или токен удалён.
    // В таком случае считаем, что токен "недействителен/истёк".
    if (!token) return true

    // Декодируем токен, чтобы достать данные из payload.
    // В decoded обычно есть поле exp (expiration time - время истечения).
    // exp хранится в секундах (Unix timestamp).
    const decoded = jwtDecode(token)

    // Проверяем срок годности токена:
    //
    // decoded.exp - время истечения в СЕКУНДАХ
    // Date.now() - текущее время в МИЛЛИСЕКУНДАХ
    //
    // Поэтому decoded.exp нужно умножить на 1000, чтобы перевести в миллисекунды.
    //
    // Пример:
    // exp = 1700000000 (секунд)
    // exp * 1000 = 1700000000000 (миллисекунд)
    //
    // Если время истечения меньше текущего времени,
    // значит токен уже просрочен -> возвращаем true.
    return decoded.exp * 1000 < Date.now()
}

/*
Важно:

1) Сервер (backend) проверяет токен "по-настоящему" (подпись, секрет, срок и т.д.).
   На клиенте мы делаем только "удобную проверку" по времени exp,
   чтобы автоматически разлогинить пользователя.

2) Если токен битый/невалидный, jwtDecode может выбросить ошибку.
   В реальном проекте можно добавить try/catch, например:

   export const isTokenExpired = (token) => {
     if (!token) return true
     try {
       const decoded = jwtDecode(token)
       return decoded.exp * 1000 < Date.now()
     } catch {
       return true // если токен сломан - считаем его недействительным
     }
   }
*/
